public class Estudante {
   private String nome;
   private int idade;
   private int pontos;

   public String getNome() {
      return nome ;
   }

   public void setNome(String estudante) {
      this.nome = estudante;
   }

   public int getIdade() {
      return idade;
   }

   public void setIdade(int idade) {
      this.idade = idade;
   }

   public int getPontos() {
      return pontos;
   }

   public void setPontos(int pontos) {
      this.pontos = pontos;
   }
}
